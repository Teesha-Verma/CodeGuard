# =====================================================================

def test_contextual_suppression_conftest_assert():
    # Assertions inside conftest.py or test files should be heavily suppressed
    file_path = "tests/conftest.py"
    code = "def test_setup():\n    assert 1 == 1\n"
    
    # A. Context Resolver Heuristics
    context = ContextResolver.resolve(file_path, code)
    assert context["is_test_file"] is True
    
    # B. Prioritization overrides
    prior = PrioritizationEngine.analyze("linter", "B101", "assert statement found", context)
    assert prior["signal_priority"] == "low"
    assert prior["issue_category"] == "style-only violations"
    assert prior["is_low_signal"] is True
    
    # C. Confidence Engine heavy discount (confidence capped or reduced to 0.15)
    finding = {"line": 2, "rule_id": "B101", "message": "assert statement found", "sources": ["linter"], "severity": "medium"}
    evidence = {"ast_nodes": [], "linter_rules": [{"tool": "bandit", "rule_id": "B101", "line": 2, "message": "assert"}]}
    conf_res = ConfidenceEngine.calculate(finding, ["bandit"], evidence, context, prior, is_changed=True)
    
    # Must be 0.15 for assert in test files
    assert conf_res["confidence"] == 0.15


