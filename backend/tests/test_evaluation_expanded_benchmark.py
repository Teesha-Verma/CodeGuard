# 2. GENERATED FILE CODES (AUTO-GENERATED HEADERS)
# =====================================================================

def test_generated_file_suppression():
    file_path = "app/models/pb_generated.py"
    code = """# Auto-generated by protobuf compiler. DO NOT EDIT!
def process_data(x):
    global_val = x
"""
    # A. Context Resolver must detect generated compiler comments
    context = ContextResolver.resolve(file_path, code)
    assert context["is_generated_file"] is True
    
    # B. Prioritization must downgrade logic risks in generated files
    prior = PrioritizationEngine.analyze("ast", "GLOBAL_MODIFICATION", "global modification", context)
    assert prior["signal_priority"] == "low"
    assert prior["issue_category"] == "style-only violations"
    assert prior["is_low_signal"] is True
    
    # C. Confidence Engine calibration
    finding = {"line": 3, "rule_id": "GLOBAL_MODIFICATION", "message": "global modification", "sources": ["ast"]}
    evidence = {"ast_nodes": [{"node_type": "Global", "line": 3, "pattern": "global_modification"}], "linter_rules": []}
    conf_res = ConfidenceEngine.calculate(finding, ["ast"], evidence, context, prior, is_changed=True)
    
    # Discount applies, capping it lower
    assert conf_res["confidence"] <= 0.60


