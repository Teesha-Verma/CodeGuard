def test_noisy_framework_schema_nuanced_maintainability():
    # Framework files like schemas or TypedDicts with low MI should get clean structural abstraction wording
    file_path = "app/schemas/user_dto.py"
    code = """from typing import TypedDict, List, Optional

class UserDTO(TypedDict):
    id: int
    name: str
    roles: List[str]
    metadata: Optional[dict]
    # Deeply nested structural setup
    preferences: dict
"""
    # A. ContextResolver identifies declarative/schema file keywords
    context = ContextResolver.resolve(file_path, code)
    assert context["is_declarative_file"] is True
    
    # B. ComplexityAnalyzer produces structural/nuanced wording rather than dramatic low-maintainability language
    analyzer = ComplexityAnalyzer()
    
    from app.core.config import get_settings
    settings = get_settings()
    old_mi_min = settings.MAINTAINABILITY_INDEX_MIN
    settings.MAINTAINABILITY_INDEX_MIN = 101  # Force breach
    
    try:
        res = analyzer.analyze(code, file_path)
        assert res["mi_exceeds_threshold"] is True
        assert "mi_interpretation" in res
        assert "structural abstractions or configurations" in res["mi_interpretation"]
        assert "No critical refactoring is recommended" in res["mi_interpretation"]
        assert "cognitive density" not in res["mi_interpretation"]
        assert "lower maintainability" not in res["mi_interpretation"]
    finally:
        settings.MAINTAINABILITY_INDEX_MIN = old_mi_min


